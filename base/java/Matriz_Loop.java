public class Matriz_Loop {
    public static void main(String[] args) {
        // Exemplo de loop "for-each" para percorrer arrays

        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
        for (String car : cars) {
            System.out.println(car);
        }

        int[] myNumber = {1, 2, 3, 4, 5};
        for (int number : myNumber) {
            System.out.println(number);

            //for (tipo variavel : array) {
            // ação
}
        }
    }
 
